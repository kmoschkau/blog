            <p>Japan is a fascinating country. I am no Japanese scholar, but some of the most passionate and poignant art I have ever experienced has come from this country and I'd like to share what I've found.</p>
			<p>Please keep in mind that most of this just scratches the surface of what Japan has to offer in terms of art and entertainment. It is not mean to be a comprehensive history of the country or even an introduction.
			It's just Japanese stuff I have enjoyed and why.
			<p>I haven't dived too deep into any specific director, or author, series etc. so I just selected a few standout pieces which I deemed most influential to me, personally.
				I mean, I'm a 22 year old guy. I can't waste all my time on weeb shit.</p>
			
			<p>To appreciate what these stories have to offer you should know just a bit of Japanese history including the eras and cultural upheavals. There are roughly nice eras that I know of (I did NOT have these memorized, please):</p>
			<ul>
			  <li>Early Japan (until 710) </li>
			  <li>Nara and Heian Periods (710-1192)</li>
			  <li>Kamakura Period (1192-1333)</li>
			  <li>Muromachi Period (1338-1573)</li>
			  <li>Azuchi-Momoyama Period (1573-1603)</li>
			  <li>Edo Period (1603-1868)</li>
			  <li>Meiji Period (1868-1912)</li>
			  <li>Taisho and Early Showa Period (1912-1945)</li>
			  <li>Postwar Period (1945)</li>
			</ul> 
			<p>For a quick 9 minute, cringey meme filled rundown on Japanese history checkout this Youtube video.</p>
			<p>A few of these eras stand out. Between the Kamakura and Edo periods were samurai and all that jazz. The Russo-Japanese War was in 1904 during the late Meiji period. World War 1 roughly inaugurates the Taisho period. And after World War 2 is the, aptly named, post-war period.</p>
			
			<p>To start off, I'd like to mention a couple prominent pieces of Japanese literature.</p>

			<p>Several noteable works I have read include:</p>
			<ul>
			  <li>Spring Snow(Sea of Fertility 1) and Confessions of a Mask by Yukio Mishima</li>
			  <li>Kokor by Natsume Soske</li>
			  <li>No Longer Human by Osamu Dazai</li>
			  <li>Snow Country by Yasunari Kawabata</li>
			  <li>Many of Haruki Murikami's works</li>
			</ul> 
			
			<p>The first books mentioned by Mishima are masterpieces. Yukio Mishima is considered one of the most important Japanese authors of the 20th century. From Wikipedia "His avant-garde work displayed a blending of modern and traditional aesthetics that broke cultural boundaries, with a focus on sexuality, death, and political change." He lived a tragic life and the profundity of his works express this deeply.
			This is a man who took his beliefs so seriously he committed suicide for them. In Snow Country, a romantic affair between two young people of nobility is set in the early years of the Taisho period. It is a tragic story. Like themes present in other Japanese stories, the personal decisions made by characters doom their futures. It is not external events and bad luck that lead to their ruin, but 
			internal troubles; misgivings in their mind that lead to wrong decisions and irreparable mistakes.
			Theme's of miscommunication, ambivalence, missed opportunity, and deep emotional pain are central to not only this novel, but many other famous works of Japanese literature. These are concepts that aren't often present in the same way in Western fiction. Often in Western lit bad luck plays the antagonist, ruining the lives of many or good luck leads to a favorable outcome. There are no Horatio Alger 'self made man' plotlines here.
			In Mishima's stories the characters do not pull themselves up by their bootstraps. They suffer. You suffer with them. You bear witness to their tragedy and suffer the pain of lost love with no analgesic. And these feelings are important. Not all literature is meant to be an escape. 
			Mishima provides a window into real human tragedy.</p>
			
			<p>Yet before Mishima came Soseki. The author of Kokor, Botcha, and I Am a Cat had a profound effect on almost every subsequent Japense writer of note. Kokoro was published in 1914, at the start of World War 1. The title can be literally translated as "heart" but the meaning goes deeper than that.
			Kokoro is used as a feeling in the novel, portraying something closer to "the heart of things". The story takes place during the Meiji and it's modernity is recognizable to contemporary readers. Following a young man begin to find his way through the new responsibilities of adulthood, family tragedy, and developing with the subtle guidance of a reserved mentor we a privy to the questioning and troubled seeking of a single individual.
			The relationship between the older mentor 'Sensei' and the young adult protagonist is not sexual, but rather emotional. It is not purely guidance that the older man provides his young friend, but a space where both can express emotions that seem to be constrained by societal pressure.
			Where this novel really stands out is in its portrayal of the cataclysm between traditionalist Japanese values and Western modernization. The younger generation has usurped the older with more free spirited culture and an openness that stands in defiance to the face of tradition. With only half the plot taking place in the 'present' and the remaining a retelling of events past, we are give another chance to contrast older values with their modern changes. 
			The most heavely present theme I picked up on was weakness. The character's lack of resolve to take action or share their thoughts and intentions leads to tragic events that condemn them to lives of quiet, personal misery. I have never read a story where the characters perpetual regret and sorrow are so vivid and present in their every action. It seems to me this loneliness is not restricted to these two distinguished characters.
			Rather, it seemed to be a societal loneliness, personified. A tragedy experienced quietly by every participant of the time.</p>
			
			<p>Osamu Dazai expressed this loneliness differently. His loneliness was that of drug addicts and miscreants, the sociopathic and morally corrupt. Taking place in postwar Japan, the novel can be compared to amoralist themes also present in postwar French literature and philosophy. The main character at a young age learns to maneuver through life by false laughter and an unending series of self deprecating jokes. He fools every one around him but one, and this other provides the reader an opportunity to see inside the pitiful mind of the young protagonist.
			As he grows older his pitifulness only increases and his self pity degrades him into a leading a sad, unfulfilling life full of meaningless sex and drug addiction. His life is full of high moments, followed by descent in to degradation and corruption. His only friend, a carefree and demoralizing influence, acts as a foil to further emphasize the main characters weakness and eventual misanthropy. At one point he watches his wife be rapped with a resignation that is quite troubling, and at another attempts to commit double suicide with a woman where he survives and she does not. 
			Suicide is a recurring theme in this novel and Dazai took his own life soon after its publication. 
			It has been said that the author may have been suffering Post Traumatic Stress Disorder during the writing, and it seems reasonable enough. There is a catharsis in the terrible, pointless life described in the books pages which is apparent to any astute reader. </p>

			<p>On a much lighter note sits the fantastic and surreal works of Haruki Murikami. He seems to have made his success through 'Westernizing' certain appealing aspects of Japanese life and spinning the stories with fantastic tales of raining fish and talking cats. 
			The appeal is obvious. Taking place in the modern era, his novels still express themes of deep emotion and internal reflection, with many of the plot lines involving characters sitting in towns or buildings waiting for events to transpire. 
			His writing may be less profound, but it provides some relief from the soul crushing narratives previously discussed and still provides a that taste of loneliness and longing that deep down everyone seeks.
			
