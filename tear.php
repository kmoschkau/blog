            <p>These are a peculiar set of words. There exists a very specific time in life when you can use them and have it make sense. There has to be a lull in a relationship, where the contact between parties is hastening to an end and the vividness of communication is disintegrating. Neither wants to be the one to ignore the last reaching out and be stuck forever as the them who broke it off. This relationship probably did not start off with long term intentions and was full of flippant words and casual meetings. Yet, the breaking off is anything but casual. It is death. To really drop out of someones life is to let them die, and these words bring fact to life. 
			Some people will look at this question and see it as mean. Or cruel. It could be asked from a point of advantage, in a teasing way where one party holds some sort of emotional power over the other, it could. But these words couldn’t be taken seriously then. They’d lose their power to create a profound moment. A moment where real acknowledgement of loss and change are actualized and nobody can ignore it and there is a tearing of something beautiful. A renting apart of human interaction and the ceasing of development of human bonding. It is not mean. It is not cruel. It is just questioning. It’s bringing to the fore something that not many people want to acknowledge and forcing them to really see what is happening. To wake up. To be drawn out of the stillness of simply living and experience that which they shirk from. The losing of another person to time and distance. To say these words requires the right circumstance. Else they will be discarded as a cruel joke. Often people don’t take the time to stop and probe into the meaning of words, especially today where the only way to be taken seriously is to not be serious. These words are serious. And they need to be asked.
